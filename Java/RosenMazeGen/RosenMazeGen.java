	private static void makeMazeRecursive(char[][]Level, int StartX, int StartY, int EndX, int EndY)

	    {

		 if((EndX > 1 && EndY > 1))
	     {
	         
	         if((EndX - StartX >= 4 && EndY - StartY >= 4))
	         {
	             Random myRandom = new Random();
	            
	             int myRow = StartY + myRandom.nextInt(EndY-StartY);
	             int myCol = StartX + myRandom.nextInt(EndX-StartX);
	             
	             for(int inc = StartX; inc <= EndX; inc++)
	             {
	                 Level[myRow][inc] = '#';
	             }
	             for(int inc = StartY; inc <= EndY; inc++)
	             {
	                 Level[inc][myCol] = '#';
	             }
	             
	             if(myCol == 1)
	                 Level[myRow][myCol + myRandom.nextInt(EndX-myCol+1)] = ' ';
	             if(myCol!= 1)
	             {
	                 Level[myRow][myCol + myRandom.nextInt(EndX-myCol+1)] = ' ';
	                 Level[myRow][StartX + myRandom.nextInt(myCol-StartX+1)] = ' ';
	             }
	             if(myRow == 1)
	                 Level[myRow-1 + myRandom.nextInt(EndY-myRow+1)][myCol] = ' ';
	             if(myRow != 1)
	             {
	                 Level[StartY + myRandom.nextInt(myRow-StartY+1)][myCol] = ' ';
	                 Level[myRow + myRandom.nextInt(EndY-myRow+1)][myCol] = ' ';
	             }
	           
	             makeMazeRecursive(Level, 1, 1, (myCol - 1), (myRow - 1));
	            
	             makeMazeRecursive(Level, (myCol + 1), 1, EndX, (myRow - 1));
	         }
	     }
	   }

	  

